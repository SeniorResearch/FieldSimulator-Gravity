import java.awt.Color;

public class Planet
{
	private float x;
	private float y;
	private int mass;
	private Color color;
	
	public Planet()
	{
		x = 0;
		y = 0;
		
		mass = 0;
		
	}
	
	public float getX()
	{
		return this.x;
	}
	
	public float getY()
	{
		return this.y;
	}

}
